CREATE TABLE ORDERS AS
SELECT DISTINCT
	ORDER_ID,
	ORDER_DATE,
	SHIP_DATE,
	SHIP_MODE,
	CUSTOMER_ID,
	SALES
FROM
	SUPERSTORE_RAW;


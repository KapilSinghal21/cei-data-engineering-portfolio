/* Q1 Sales > Average */
SELECT
	*
FROM
	ORDERS
WHERE
	SALES > (
		SELECT
			AVG(SALES)
		FROM
			ORDERS
	);

/* Q2 Highest Order per Customer */
SELECT
	*
FROM
	ORDERS O
WHERE
	SALES = (
		SELECT
			MAX(SALES)
		FROM
			ORDERS
		WHERE
			CUSTOMER_ID = O.CUSTOMER_ID
	);

/* Q3 Total Sales per Customer (CTE) */
WITH
	CUSTOMER_SALES AS (
		SELECT
			CUSTOMER_ID,
			SUM(SALES) AS TOTAL_SALES
		FROM
			ORDERS
		GROUP BY
			CUSTOMER_ID
	)
SELECT
	*
FROM
	CUSTOMER_SALES;

/* Q4 Above Average Customers */
WITH
	CUSTOMER_SALES AS (
		SELECT
			CUSTOMER_ID,
			SUM(SALES) AS TOTAL_SALES
		FROM
			ORDERS
		GROUP BY
			CUSTOMER_ID
	)
SELECT
	*
FROM
	CUSTOMER_SALES
WHERE
	TOTAL_SALES > (
		SELECT
			AVG(TOTAL_SALES)
		FROM
			CUSTOMER_SALES
	);

/* Q5 Rank Customers (Window) */
WITH
	CUSTOMER_SALES AS (
		SELECT
			CUSTOMER_ID,
			SUM(SALES) AS TOTAL_SALES
		FROM
			ORDERS
		GROUP BY
			CUSTOMER_ID
	)
SELECT
	*,
	RANK() OVER (
		ORDER BY
			TOTAL_SALES DESC
	) AS RANK
FROM
	CUSTOMER_SALES;

/* Q6 Row Number per Customer */
SELECT
	*,
	ROW_NUMBER() OVER (
		PARTITION BY
			CUSTOMER_ID
		ORDER BY
			SALES DESC
	) AS ROW_NUM
FROM
	ORDERS;

/* Q7 Top 3 Customers */
WITH
	CUSTOMER_SALES AS (
		SELECT
			CUSTOMER_ID,
			SUM(SALES) AS TOTAL_SALES
		FROM
			ORDERS
		GROUP BY
			CUSTOMER_ID
	)
SELECT
	*
FROM
	(
		SELECT
			*,
			RANK() OVER (
				ORDER BY
					TOTAL_SALES DESC
			) AS RANK
		FROM
			CUSTOMER_SALES
	) T
WHERE
	RANK <= 3;

/* COMBINED QUERY */
WITH
	CUSTOMER_SALES AS (
		SELECT
			CUSTOMER_ID,
			SUM(SALES) AS TOTAL_SALES
		FROM
			ORDERS
		GROUP BY
			CUSTOMER_ID
	)
SELECT
	C.CUSTOMER_NAME,
	CS.TOTAL_SALES,
	RANK() OVER (
		ORDER BY
			CS.TOTAL_SALES DESC
	) AS RANK
FROM
	CUSTOMER_SALES CS
	JOIN CUSTOMERS C ON CS.CUSTOMER_ID = C.CUSTOMER_ID;

